	<h2 ID="CfP" name="Call_for_Particiption">Call for Participation FOSSGIS 2024</h2>

	<p>Die FOSSGIS-Konferenz ist die größte deutschsprachige Konferenz für Open Source Software für Geoinformationen und OpenStreetMap und wird vom 20.-23. März 2024 an der Technischen Universität Hamburg in Harburg stattfinden.   
Konferenzveranstalter sind der gemeinnützige FOSSGIS e.V., die OpenStreetMap-Community in Kooperation mit dem Institut für Verkehrsplanung und Logistik der Technischen Universität Hamburg und der HafenCity Universität.</p>

	<p>Freie quelloffene Software, Open Data und Open Science leisten einen wichtigen Beitrag zur Stärkung der Digitalen Souveränität. Ziel der jährlich stattfindenden Konferenz ist die Verbreitung von Freier Open Source Software (FOSS) für Geoinformationssysteme (GIS). Hier treffen sich Anwender:innen, Entwickler:innen und Interessierte zum gemeinsamen Austausch über GIS-Software, OpenStreetMap und neue Projekte und Themen mit Geodatenbezug.
Mit Vorträgen, Workshops, Demo-Sessions, Anwendertreffen und Gelegenheiten zum Netzwerken und Austausch wird die Veranstaltung vorwiegend in Präsenz stattfinden. Die gleichzeitig stattfindende Firmen- und Projektausstellung ermöglicht einen direkten Kontakt zu Dienstleistern und Projekten.</p>

	<p>Samstag, der 23. März ist dem "OSM-Event" gewidmet, einer Unkonferenz der deutschsprachigen OpenStreetMap-Community. Es finden Vorträge und Diskussionsrunden zu aktuellen Themen statt.</p>

    <p>Der <a href="https://pretalx.com/fossgis2024/cfp">Call for Participation für die FOSSGIS 2024</a> ist bis zum 6. November 2023 geöffnet (keine Verlängerung).</p>

    <h2>Themenbereiche</h2>

