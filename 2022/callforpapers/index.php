	<h2>Hybride Veranstaltung, Streaming und Aufzeichnung</h2>

	<p>Die Konferenz wird als hybride Veranstaltung ausgerichtet. Sie können sowohl vor Ort als auch aus der Ferne (remote) teilnehmen. Aus diesem Grund ist es erforderlich, dass alle Vortragenden dem Streaming und der Aufzeichnung ihrer Beiträge zustimmen.</p>

	<p>Sollte die Infektionslage der Durchführung einer Konferenz vor Ort im Wege stehen, wird die FOSSGIS eine reine Online-Konferenz werden. Um Programmänderungen zu vermeiden, erwarten wir daher von allen Beitragenden, in solch einem Fall aus der Ferne vorzutragen.<br>
Nach Erfahrung der FOSSGIS 2021 präferieren wir eine Aufzeichnung vorab, Sie sollten trotzdem während der Vortragszeit anwesend sein und im Anschluss an den Vortrag für Fragen zur Verfügung stehen.</p>

	<p>Wir bitten Sie bei der Einreichung anzugeben, ob Sie planen vor Ort oder aus der Ferne teilzunehmen. <br>
Für diejenigen, die vor Ort teilnehmen wollen, ist zu berücksichtigen, dass Sie zum Zeitpunkt der Konferenz einen entsprechenden Genesenen- oder Impfnachweis vorweisen müssen. Es ist geplant die Konferenz unter der 2G-Regel stattfinden zu lassen.</p>



