	<h2>Unterstützung der Konferenzorganisation</h2>

	<p>Seit vielen Jahren findet die FOSSGIS-Konferenz im deutschsprachigen Raum an wechselnden Standorten statt.<br>
Ohne die Unterstützung durch zahlreiche Mitorganisierende vor der Veranstaltung und Helfer:innen während der Veranstaltung wäre eine FOSSGIS-Konferenz nicht möglich.<br>
	<p>Die Konferenz wird vorwiegend durch ehrenamtliches Engagement getragen. Dies ermöglicht eine flexible Konferenzorganisation und vielfältiges Rahmenprogramm. Die Aufgaben werden auf mehrere Schultern verteilt.</p>

	<p><b>Damit dies so bleiben kann, brauchen wir Deine Unterstützung!<sup>*</sup></b><br>
		<span style="color:grey">* wer in der Community aktiv ist, wird geduzt :-)
         </span></p>
	
	<p>Auf dieser Seite kommen hin und wieder neue Informationen hinzu, also gerne wiederkommen.</p>

