	<h2 ID="CfP" name="Call_for_Particiption">Call for Participation FOSSGIS 2023</h2>

	<p>Die FOSSGIS-Konferenz ist die größte deutschsprachige Konferenz für Open Source Software für Geoinformationen  und OpenStreetMap und wird vom 15.-18. März 2023 an der Humboldt-Universität zu Berlin in den Räumen des Erwin-Schrödinger-Zentrums in Berlin-Adlershof stattfinden.<br>
Konferenzveranstalter sind der gemeinnützige FOSSGIS e.V., die OpenStreetMap-Community und das Geographische Institut der Humboldt-Universität zu Berlin.</p>

	<p>Freie quelloffene Software, Open Data und Open Science leisten einen wichtigen Beitrag zur Stärkung der Digitalen Souveränität. 
Ziel der jährlich stattfindenden Konferenz ist die Verbreitung von Freier Open Source Software (FOSS) für Geoinformationssysteme (GIS). Hier treffen sich Anwender:innen, Entwickler:innen und Interessierte zum gemeinsamen Austausch über GIS-Software, OpenStreetMap und neue Projekte mit Geodatenbezug.<br>
Mit Vorträgen, Workshops, Demo-Sessions, Anwendertreffen und Gelegenheiten zum Netzwerken und Austausch wird die Veranstaltung vorwiegend in Präsenz stattfinden. Die gleichzeitig stattfindende Firmen- und Projektausstellung ermöglicht einen direkten Kontakt zu Dienstleistern und Projekten.</p>

	<p>Samstag, der 18. März ist dem "OSM-Event" gewidmet, einer Unkonferenz der deutschsprachigen OpenStreetMap-Community. Es finden Vorträge und Diskussionsrunden zu aktuellen Themen statt.</p>

    <p>Der <a href="https://pretalx.com/fossgis2023/cfp">Call for Participation FOSSGIS 2023</a> ist bis zum 07. November 2022 geöffnet (keine Verlängerung).</p>

    <h2>Themenbereiche</h2>

	<p>Die FOSSGIS-Konferenz bedient ein weites Themenfeld, welches unter anderem die folgenden Aspekte umfasst:</p>
	<ul>
    	<li>Grundlagen Open-Source-GIS und OpenStreetMap</li>
    	<li>Neuigkeiten aus den Open-Source-Projekten</li>
    	<li>Praxisberichte</li>
    	<li>Daten, Datenbanken und Datenprozessierung</li>
    	<li>Routing und Mobilität</li>
		<li>Raum-zeitliche Analysen und Modelle</li>
    	<li>Rasterdaten und Fernerkundung</li>
    	<li>3D, Drohnen, LIDAR</li>
    	<li>Kartographie und Visualisierung</li>
    	<li>Offene Standards, z.B. INSPIRE, OGC</li>
		<li>Geodatenmanagement</li>
    	<li>Open Data, Datenschutz und Lizenzen</li>
    	<li>Ausbildung und Lehre</li>
     </ul>


  	<h2>Einreichungstypen</h2>

	<p>Das Konferenzprogramm beinhaltet folgende Formate:</p>
	<ul>
		<li><strong>Lightning Talk</strong><br>
Ein Lightning Talk bietet Gelegenheit neue Software, neue Projekte oder neue Ideen in 5 Minuten zu präsentieren. Es ist möglich, noch kurz vor und während der Konferenz einen LT einzureichen, sofern Kapazitäten im Programm verfügbar sind.</li>

		<li><strong>Vortrag</strong><br>
Vorträge bilden den Hauptteil der Konferenz. Ein Vortrag ist 20 Minuten lang. Danach sind 5 Minuten Zeit für Fragen.</li>
		
		<li><strong> Vortrag mit Paper im Academic Track</strong><br> 
Begutachtung im Peer-Review-Verfahren durch das Academic Track-Komitee mit dem Ziel einer zitierfähigen Veröffentlichung eines Full Papers (6-8 Seiten). 
			Der einzureichende Abstract sollte ca. 3.000 Zeichen umfassen.</li>

		<li><strong>Demo-Session</strong><br>
Die Demo-Session dient der Vorführung von Software oder Prozessen ("Live-Hacking") und ist 45 Minuten + 10 Minuten Diskussion lang.</li>

		<li><strong>Workshop</strong><br>
Workshops finden von Mittwoch bis Freitag parallel zu den Vorträgen statt. Die Teilnahme am 90-minütigen Workshop ist kostenpflichtig, die Zahl der Teilnehmenden ist begrenzt.  Selbstverständlich sind zwei Betreuende möglich, insbesondere, wenn der Workshop stark nachgefragt ist.</li>

		<li><strong>Anwendertreffen/BoF</strong><br>
Anwendertreffen  oder "Birds of a Feather"-Sessions (BoF- ) ermöglichen es Gleichgesinnten  und -interessierten sich konzentriert und selbstorganisiert zu einem bestimmten Thema in einer 60-minütigen Session auszutauschen. Eine Session kann eingereicht (sie ist dann im Programmheft zu sehen) oder auch noch kurz vor und während der Konferenz angemeldet werden.</li>

		<li><strong>Expert:innenfragestunde</strong><br>
Das 2022 eingeführte neue Format der Expert:innenfragestunde wird fortgeführt. Die Idee ist, dass Expert:innen für 60 Minuten für Fragen von Teilnehmenden zur Verfügung stehen.</li>


		<li><strong>Sonstiges</strong><br>
Ihre Einreichung passt nicht zu einem der Einreichungstypen? Dann setzen Sie sich bitte vor dem Ende der Einreichungsfrist mit dem Programm-Komitee per <a href="mailto:pko@fossgis.de? subject=Frage%an%das%Programmkomitee"> per E-Mail</a> in Verbindung.</li>
	</ul>


	<h2>Bewertungskriterien</h2>

	<p>Bei der Bewertung der Einreichungen finden folgende Kriterien Anwendung:</p>
		<ul>
			<li>Die Inhalte des Beitrags dienen der Wissensvermittlung und dem Erfahrungsaustausch bezüglich aktueller Themen.</li>

			<li>Bei der Einreichung muss es um freie Software oder freie Daten gehen.</li>
				<ul>
				<li>Software ist frei, wenn ihre Lizenzbedingungen von der <a href="https://www.gnu.org/licenses/license-list.html">Free Software Foundation</a> als frei anerkannt werden und der Quellcode öffentlich verfügbar ist.</li>
				<li>Daten sind frei, wenn ihre Lizenzbedingungen der <a href="https://opendefinition.org/">Open Definition</a> entsprechen.</li>
				<li>Der FOSSGIS e.V. unterstützt die Initiative <a href="https://publiccode.eu/de/">Public Money, Public Code</a>. Wir ermuntern alle Projekte und Organisationen, die mit öffentlichen Mitteln freie Software finanzieren, sich und ihre Projekte / Software vorzustellen und von ihren Erfahrungen zu berichten.</li>
				<li>Selbstverständlich freuen wir uns über Beiträge, die die Migration von unfreier zu freier Software oder die Nutzung proprietärer Formate/Schnittstellen mit freier Software thematisieren.</li>
				</ul>
			<li>Vorträge sollten einen Neuigkeitswert bieten.</li>
			<li>Die Einreichung wird nach inhaltlichen Kriterien bewertet. Daher ist für die Bewertung eine aussagekräftige Beschreibung (die etwas mehr Information als die Zusammenfassung enthält) wichtig.</li>
			<li>Eine Einreichung muss von einer oder mehreren natürlichen Personen erfolgen.</li>
			<li>Beiträge von Sponsoren werden nicht bevorzugt behandelt, die Vorträge dienen nicht der reinen Firmenpräsentation.</li>
			<li>Es wird wieder ein Community-Voting stattfinden. Das Ergebnis wird vom Programm-Komitee als Stimmungsbild aus der Community einbezogen.</li>

			<h3>Zusätzlich für Academic Track</h3>
			<p>Einreichungen - bzw. die Full Paper - im Academic Track werden zusätzlich nach gängigen wissenschaftlichen Kriterien bewertet.</p>
			
			<h3>Zusätzlich für Workshops:</h3>
			<li>Wir bevorzugen Workshops zu aktuellen und nachgefragten Themen.</li>
			<li>Die geschätzte Nachfrage auf Basis der Vorjahre hat einen erheblichen Einfluss auf die Auswahl.</li>
			<li>Die Workshops finden ausschließlich vor Ort in Berlin statt.</li>
	</ul>
